package org.zaproxy.zap.extension.openapi;

import java.util.ArrayList;
import java.util.List;
import org.parosproxy.paros.model.HistoryReference;

public class OpenApiResults {

    private List<String> errors = new ArrayList<>();
    private List<HistoryReference> historyReferences = new ArrayList<>();

    public List<String> getErrors() {
        return errors;
    }

    public void setErrors(List<String> errors) {
        this.errors = errors;
    }

    public void addError(String error) {
        this.errors.add(error);
    }

    public void addHistoryReference(HistoryReference href) {
        historyReferences.add(href);
    }

    public List<HistoryReference> getHistoryReferences() {
        return historyReferences;
    }
}
